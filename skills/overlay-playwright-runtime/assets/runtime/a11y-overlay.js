  function sourceBlock(items) {
    return (items || [])
      .map((item) => {
        if (!item || !item.label) return '';
        const sourceType = item.type ? ` (${formatFindingType(item.type) || titleCase(item.type)})` : '';
        return item.url ? `${item.label}${sourceType}\n${item.url}` : `${item.label}${sourceType}`;
      })
      .filter(Boolean)
      .join('\n\n');
  }

