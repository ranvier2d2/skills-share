      let findingType = 'standard';
      let severity = 'pass';
      let confidence = 'high';
      let label = '';
      let color = COLOR.heading;
      let opts = { dashed: true, fill: 'rgba(34,211,238,0.08)' };
      let summary = '';
      let whyFlagged = '';
      let suggestedFix = '';
      let showInCanvas = false;
